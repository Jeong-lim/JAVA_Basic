 
public class Ex1 {

	public static void main(String[] args) {
		int year = 0;
		int age = 14;
		
		System.out.println(year);
		System.out.println(age);
		
		year = age + 2000; // year에 age 값과 2000을 더한다
		age = age + 1; // 저장된 age 값에 1을 더한다
		
		System.out.println(year);
		System.out.println(age);
		
		// System.out.println 출력할 때 sysout하고 Enter
	}

}