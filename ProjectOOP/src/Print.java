
class Print {
	public static String delimiter  = "";
	// static 이라는 것은 String은 Class의 소속이라는 것
	// 즉 static은 class의 소속 
	public static void A() {
		System.out.println(delimiter);
		System.out.println("A");
		System.out.println("A");
	}
	public static void B() {
		System.out.println(delimiter);
		System.out.println("B");
		System.out.println("B");
	}
}